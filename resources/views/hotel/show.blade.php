                        item.style.transform = `translateX(-${index * 100}%)`;
                    });
                }

                prevBtn.addEventListener('click', () => {
                    currentIndex = (currentIndex > 0) ? currentIndex - 1 : items.length - 1;
                    showItem(currentIndex);
                });

                nextBtn.addEventListener('click', () => {
                    currentIndex = (currentIndex < items.length - 1) ? currentIndex + 1 : 0;
                    showItem(currentIndex);
                });

                // Auto-rotate every 5 seconds
                setInterval(() => {
                    currentIndex = (currentIndex < items.length - 1) ? currentIndex + 1 : 0;
                    showItem(currentIndex);
                }, 5000);
            });

            // Room filtering and search functionality
            const searchInput = document.getElementById('searchRoom');
            const filterButtons = document.querySelectorAll('.filter-btn');
            const roomItems = document.querySelectorAll('.room-item');
            const noResults = document.getElementById('noResults');
            const roomCount = document.getElementById('roomCount');

            function updateRoomDisplay() {
                const searchTerm = searchInput.value.toLowerCase();
                const activeFilter = document.querySelector('.filter-btn.active')?.dataset.filter || '';

                let visibleCount = 0;

                roomItems.forEach(room => {
                    const roomName = room.dataset.name;
                    const roomVendor = room.dataset.vendor;

                    // Check if matches search term and filter
                    const matchesSearch = roomName.includes(searchTerm);
                    const matchesFilter = activeFilter === '' || roomVendor === activeFilter;

                    if (matchesSearch && matchesFilter) {
                        room.style.display = 'block';
                        room.classList.remove('hidden');
                        visibleCount++;
                    } else {
                        room.style.display = 'none';
                        room.classList.add('hidden');
                    }
                });

                // Update room count
                roomCount.textContent = `Menampilkan ${visibleCount} kamar tersedia`;

                // Show/hide no results message
                if (visibleCount === 0) {
                    noResults.classList.remove('hidden');
                } else {
                    noResults.classList.add('hidden');
                }
            }

            // Event listeners
            searchInput.addEventListener('input', updateRoomDisplay);

